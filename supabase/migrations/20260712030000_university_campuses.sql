CREATE TABLE IF NOT EXISTS public.university_campus (
  campus_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  university_id BIGINT NOT NULL REFERENCES public.university(university_id) ON DELETE CASCADE,
  name VARCHAR(160) NOT NULL,
  address TEXT NOT NULL DEFAULT '',
  contact_info VARCHAR(160) NOT NULL DEFAULT '',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT university_campus_university_name_key UNIQUE (university_id, name)
);

ALTER TABLE public.student
  ADD COLUMN IF NOT EXISTS campus_id BIGINT
  REFERENCES public.university_campus(campus_id) ON DELETE SET NULL;

CREATE INDEX IF NOT EXISTS student_campus_id_idx ON public.student(campus_id);

